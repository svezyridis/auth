package sphy.auth;

public class Role {

    private Integer ID;
    private String role;

    public Integer getID() {
        return ID;
    }

    public String getRole() {
        return role;
    }

    public void setID(Integer ID) {
        this.ID = ID;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
